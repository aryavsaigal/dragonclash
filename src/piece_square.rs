pub const fn mirror_table(table: [i32; 64]) -> [i32; 64] {
    let mut mirrored = [0; 64];
    let mut i = 0;
    while i < 64 {
        let rank = i / 8;
        let file = i % 8;
        let new_index = (7 - rank) * 8 + (7 - file); // flip vertically + horizontally
        mirrored[new_index] = table[i];
        i += 1;
    }
    mirrored
}

